package androidx.compose.mpp.demo

import androidx.compose.foundation.background
import androidx.compose.foundation.layout.Box
import androidx.compose.foundation.layout.fillMaxSize
import androidx.compose.foundation.layout.size
import androidx.compose.foundation.lazy.LazyColumn
import androidx.compose.foundation.lazy.rememberLazyListState
import androidx.compose.material3.Text
import androidx.compose.runtime.Composable
import androidx.compose.runtime.remember
import androidx.compose.ui.Alignment
import androidx.compose.ui.Modifier
import androidx.compose.ui.graphics.Color
import androidx.compose.ui.unit.dp
import kotlin.random.Random

@Composable
fun ScrollPhysicsPlayground() {
    val state = rememberLazyListState()

    LazyColumn(Modifier.fillMaxSize(), horizontalAlignment = Alignment.CenterHorizontally, state = state) {
        items(500) {
            Boxes(it)
        }
    }
}

@Composable
fun Boxes(id: Int) {
    Box(Modifier.size(200.dp).background(remember { Color(Random.nextInt()) })) {
        Text("$id", Modifier.align(Alignment.Center))
    }
}